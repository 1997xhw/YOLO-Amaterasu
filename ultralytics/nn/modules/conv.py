class Concat_dropoutV2(nn.Module):
    """Concatenate a list of tensors along a specified dimension with learnable dropout."""

    def __init__(self, dimension=1, ch=None, dropout_rate=0.5):
        """Initialize the ConcatDropout module.

        Args:
            dimension (int): Dimension along which to concatenate.
            ch (list): List of input channels for each input tensor.
            dropout_rate (float): Dropout probability.
        """
        super().__init__()
        self.d = dimension
        self.ch = ch
        self.dropout = nn.Dropout(dropout_rate)
        total_channels = sum(ch)
        self.conv = nn.Conv2d(total_channels, ch[0], kernel_size=1, stride=1)
        # Learnable parameter for soft routing
        self.weight = nn.Parameter(torch.zeros(1))

    def forward(self, x):
        """Forward pass for the ConcatDropout module.

        Args:
            x (list): List of input tensors.
        """
        # Concatenate inputs
        concatenated = torch.cat(x, self.d)
        # Apply convolution
        concatenated = self.conv(concatenated)
        # Apply dropout
        concatenated = self.dropout(concatenated)
        # Soft routing using sigmoid
        weight = torch.sigmoid(self.weight)
        # Weighted sum of concatenated output and the first input tensor
        output = weight * concatenated + (1 - weight) * x[0]
        return output
